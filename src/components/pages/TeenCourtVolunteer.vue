      <h1 class="pt-3">Teen Court Volunteer Job Descriptions</h1>
      <v-container>
        <p>
          <b>JUROR</b><br />
          Participates as a member of a six person to twelve-person jury. Teen
          jury determines the sentence of defendant after reviewing the facts of
          the case. Using the established sentence grid, the jury must come to a
          unanimous decision, keeping in mind the assigned consequences are
          designed to hold the youth responsible for his/her actions, and
          requires the youth to be accountable to the community and victim.
        </p>

        <p>
          <b>ATTORNEY ROLES</b><br />
          Attorneys are assigned in teams of two and sometimes three (two for
          prosecution, two for defense) per case. A new and inexperienced
          attorney is assigned with an experienced attorney for the first few
          cases and/or until the youth is comfortable. Youth acting as attorneys
          must be willing to come to court prepared to present their case. Case
          paperwork must be handled in a confidential manner and shredded upon
          completion of case. Defense attorneys are asked to call their clients
          prior to court.
        </p>

        <p>
          <b>PROSECUTOR</b><br />
          The prosecutor's role is to represent the state; to represent the
          public interest by requesting a sentence that is strict but fair; to
          avoid playing Judge, and to maintain respect for the judicial system.
        </p>

        <p>
          <b>DEFENSE ATTORNEY</b><br />
          The role of defense attorney is to represent a client zealously within
          the bounds of the law. The defense attorney must not represent persons
          if his/her ability to represent them effectively is impaired. Like the
          prosecutor, the defense attorney has the duty to maintain public
          respect for the judicial system.
        </p>

        <p>
          <b>MASTER JURY JUDGE</b><br />
          The Master Jury Judge is in charge of the master jury hearing and
          follows the Master Jury Script. The Judge will ensure that questioning
          of the witness by jurors is thorough and on-track and that each juror
          asks a minimum of one question. The Judge stays during deliberations
          and makes certain that the discussion pertains to the case. The Judge
          also administers after-sentencing instructions to defendant.
        </p>

        <p>
          <b>BAILIFF</b><br />
          The Bailiff calls the jurors to the jury box at the start of each
          hearing. Upon completion of the hearing, the bailiff leads the jury to
          the deliberation room and monitors the jury at all times. After
          deliberation, the bailiff leads the jury back into the courtroom. The
          bailiff is also responsible for maintaining order in the courtroom and
          assisting the Judge upon request.
        </p>

        <p>
          <b>COURT CLERK</b><br />
          The clerk sits next to the Judge during the entire court session. The
          clerk completes the court docket as the cases progress. He/she also
          assists the Judge upon request.
        </p>

        <p>
          The clerk assists the Teen Court coordinator prior to court sessions
          with registration. As jurors. volunteers, and defendants enter the
          courtroom, the clerk helps with jury assignment and answers questions.
